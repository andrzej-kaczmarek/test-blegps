void
oled_write_cmd(uint8_t byte)
{
    hal_gpio_write(PIN_CS, 1);
    hal_gpio_write(PIN_DC, 0);
    hal_gpio_write(PIN_CS, 0);
    hal_spi_tx_val(0, byte);
    hal_gpio_write(PIN_CS, 1);
}

static void
oled_write_data(uint8_t byte)
{
    hal_gpio_write(PIN_CS, 1);
    hal_gpio_write(PIN_DC, 1);
    hal_gpio_write(PIN_CS, 0);
    hal_spi_tx_val(0, byte);
    hal_gpio_write(PIN_CS, 1);
}

void
oled_setup(void)
{
    struct hal_spi_settings hss = {
            .data_order = HAL_SPI_MSB_FIRST,
            .data_mode = HAL_SPI_MODE0,
            .baudrate = 500,
            .word_size = HAL_SPI_WORD_SIZE_8BIT,
    };

    hal_gpio_init_out(PIN_CS, 0);
    hal_gpio_init_out(PIN_DC, 0);
    hal_gpio_init_out(PIN_RES, 0);

    hal_spi_config(0, &hss);
    hal_spi_enable(0);

    hal_gpio_write(PIN_CS, 1);
    hal_gpio_write(PIN_DC, 0);

    hal_gpio_write(PIN_RES, 1);
    os_time_delay(1 * OS_TICKS_PER_SEC / 1000);
    hal_gpio_write(PIN_RES, 0);
    os_time_delay(10 * OS_TICKS_PER_SEC / 1000);
    hal_gpio_write(PIN_RES, 1);

    oled_write_cmd(0xAE); // Set Display OFF
    oled_write_cmd(0xA1); // Set Segment Re-map
    oled_write_cmd(0xC8); // Set COM Output Scan Direction
    oled_write_cmd(0xA8); // Set Multiplex Radio
    oled_write_cmd(0x3F);
    oled_write_cmd(0xD3); // Set Display Offset
    oled_write_cmd(0x00);
    oled_write_cmd(0xD5); // Set Display Clock Divide Ratio/Oscillator Frequency
    oled_write_cmd(0x80);
    oled_write_cmd(0xD9); // Set Pre-charge Period
    oled_write_cmd(0xF1);
    oled_write_cmd(0x8D); // Charge Pump Setting
    oled_write_cmd(0x14);
    oled_write_cmd(0xAF); // Set Display ON

    oled_clear();
}

void
oled_page(int page)
{
    oled_write_cmd(0xB0 + page);
    oled_write_cmd(0x00);
    oled_write_cmd(0x10);
}

void
oled_clear(void)
{
    int i, j;

    for (i = 0; i < 8; i++) {
        oled_page(i);
        for (j = 0; j < 128; j++) {
            oled_write_data(0);
        }
    }
}

void
oled_putc(uint8_t c)
{
    int i;

    for (i = 0; i < 5; i++) {
        oled_write_data(font57[c * 5 + i]);
    }

    oled_write_data(0);
}

void
oled_puts(const char *s)
{
    while (*s) {
        oled_putc(*s);
        s++;
    }
}

void
oled_putd(uint8_t d)
{
    oled_write_data(d);
}

void
oled_printfln(int index, const char *fmt, ...)
{
    char buf[22];
    va_list ap;
    int len;

    memset(buf, ' ', sizeof(buf));

    va_start(ap, fmt);
    len = vsnprintf(buf, sizeof(buf), fmt, ap);
    va_end(ap);

    if (len < sizeof(buf)) {
        buf[len] = ' ';
    }
    buf[21] = '\0';

    oled_page(index);
    oled_puts(buf);
}
